#include <cstdio>
#include <cstring>
#include <iostream>
#define MAXN 505

int pos;
char ch[MAXN];

int main()
{
	while(ch[0] = getchar())
	{
		while(ch[pos] != '\n') ch[++ pos] = getchar();
		pos = 0;
		for(int i = 0; i < MAXN; ++ i)
		{
			if(ch[i] == '(') break;
			std :: cout << ch[i];
		}
	}
}
